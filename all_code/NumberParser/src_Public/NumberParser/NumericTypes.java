package NumberParser;

/**Enum aiming to simplify the in-built Java recognition of numeric types/classes.**/
public enum NumericTypes
{ 
	/**No type.**/
	None, 
	/**Double or double.**/
	Double, 
	/**Float or float.**/
	Float, 
	/**Long or long.**/
	Long, 
	/**Integer or int.**/
	Integer, 
	/**Short or short.**/
	Short, 
	/**Byte or byte.**/
	Byte, 
	/**Character or char.**/
	Character 
}