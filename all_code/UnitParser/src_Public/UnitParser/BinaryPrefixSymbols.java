package UnitParser;

/**
Contains the official symbols of all the binary prefixes.
All the analyses involving these strings treat them as case sensitive.
**/
public class BinaryPrefixSymbols
{
    /**Yobi symbol. 2^80.**/
    public final static String Yobi = "Yi";
    /**Zebi symbol. 2^70.**/           
    public final static String Zebi = "Zi";
    /**Exbi symbol. 2^60.**/                     
    public final static String Exbi = "Ei";
    /**Pebi symbol. 2^50.**/                               
    public final static String Pebi = "Pi";
    /**Tebi symbol. 2^40.**/                                
    public final static String Tebi = "Ti";
    /**Gibi symbol. 2^30.**/                                            
    public final static String Gibi = "Gi";
    /**Mebi symbol. 2^20.**/                                           
    public final static String Mebi = "Mi";
    /**Kibi symbol. 2^10.**/                                           
    public final static String Kibi = "Ki";
};

